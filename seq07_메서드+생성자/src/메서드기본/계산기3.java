package 메서드기본;

public class 계산기3 {
	public int add(int x, int y) {
		int result = x + y; 
		return result;
	}
	public int minus(int x, int y) {
		int result = x - y;
		return result;
	}
	public int mul(int x, int y) {
		int result = x * y;
		return result;
	}
	public int div(int x, int y) {
		int result = x / y;
		return result;
	}
}
