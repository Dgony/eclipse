package 메서드기본;

public class 계산기4 {
	public int sum(int count, int price) {
		int result = count * price;
		return result;
	}
	public int div(int sum, int person) {
		int result = sum / person;
		return result;
	}
	public int total(int sum1, int sum2) {
		int result = sum1 + sum2;
		return result;
	}
}
