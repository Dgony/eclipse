package 자바기본;

public class 비교연산 {

	public static void main(String[] args) {
		// 비교연산자는 결과에 따라 무조건 논리형 결과만 반환
		int x = 200;
		int y = 100;
		
		System.out.println("일치? " + (x == y));
		System.out.println("불일치? " + (x != y));
		System.out.println("이상? " + (x >= y));
		System.out.println("초과? " + (x > y));
		

	}

}
